# Control Flow & Loops
x = 10

if x > 0:
    print("Positive number")
elif x == 0:
    print("Zero")
else:
    print("Negative number")

# For Loop
print("For loop:")
for i in range(5):
    print("i =", i)

# While Loop
print("While loop:")
count = 0
while count < 3:
    print("count =", count)
    count += 1
